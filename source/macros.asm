ifndef macros_asm
macros_asm = 0                                                                        ; header guard variable

AssertNotEq macro reg:req, flag:req
    LOCAL .ok
    cmp reg, flag
    jne .ok
    int 3
    .ok:
endm

AssertEq macro reg:req, flag:req
    LOCAL .ok
    cmp reg, flag
    je .ok
    int 3
    .ok:
endm

endif
